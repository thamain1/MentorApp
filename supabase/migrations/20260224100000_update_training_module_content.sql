UNDERSTANDING BOUNDARIES

Boundaries protect everyone in a mentoring relationship — you, your mentee, and the program. They are not barriers to connection; they are the structure that makes real connection safe and sustainable.

WHAT ARE BOUNDARIES?
Boundaries are the clear lines that define what is appropriate and what is not in your role as a mentor. They include physical, emotional, digital, and time-related limits.

WHY BOUNDARIES MATTER:
1. They protect your mentee from harm
2. They protect you from misunderstandings or false accusations
3. They model healthy relationship dynamics — a life skill your mentee needs
4. They keep the relationship professional and sustainable long-term

PHYSICAL BOUNDARIES:
- Meet in public, observable places — not in your home or his
- Side hugs are appropriate; prolonged or tight embraces are not
- Never be alone with your mentee in a private, enclosed space
- Transportation: if you drive your mentee, inform a parent/guardian and program admin

EMOTIONAL BOUNDARIES:
- You are a mentor, not a best friend or a parent substitute
- Do not share personal problems that put your mentee in a caretaker role
- Maintain your own life — do not become overly available or enmeshed
- Do not make your mentee responsible for your emotional wellbeing

DIGITAL BOUNDARIES:
- Communicate only through approved program channels (the app)
- Do not connect on personal social media accounts
- Do not share your personal phone number
- Do not text or DM outside the program platform

TIME BOUNDARIES:
- Sessions should be scheduled, not spontaneous
- Stick to agreed session times — do not let sessions run extremely long habitually
- Set clear expectations about response times for messages

COMMON BOUNDARY MISTAKES:
- "We're close enough that the rules don't apply to us"
- Giving gifts of significant value
- Sharing too much about your own relationship struggles
- Being the first person they call for everything

IF A BOUNDARY FEELS LIKE IT HAS BEEN CROSSED:
Name it calmly and directly. "I care about you and I want to keep our relationship healthy. Let's keep things between us through the app." Then report it to program admin if needed.

REFLECTION QUESTIONS:
- Are there any areas where your current mentoring relationship feels unclear on boundaries?
- How would you handle it if your mentee tried to friend-request you on social media?
