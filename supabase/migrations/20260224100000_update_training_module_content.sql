RECOGNIZING WARNING SIGNS

As a mentor, you are often one of the most consistent adult figures in your mentee's life. That means you may be the first person to notice when something is wrong. Knowing what to look for can make a critical difference.

YOUR ROLE IS NOT DIAGNOSIS — IT IS OBSERVATION:
You are not a clinician. You do not need to determine what is wrong. You need to notice changes and report concerns to program staff, who will take it from there.

BEHAVIORAL WARNING SIGNS:
These changes in behavior — especially sudden ones — warrant attention:
- Withdrawal from the relationship or becoming unusually quiet
- Significant change in energy (extreme lethargy or hyperactivity)
- Increased agitation, anger, or crying
- Declining school performance (mentioned in sessions or by family)
- Loss of interest in things he used to care about
- Secretiveness about where he is going or who he is with

PHYSICAL SIGNS:
- Unexplained bruises, cuts, or marks
- Signs of poor nutrition, hygiene, or sleep deprivation
- Appearing intoxicated or under the influence
- Dramatic weight loss or gain

MENTAL HEALTH WARNING SIGNS:
- Expressing hopelessness ("What's the point?") or worthlessness
- Talking about death or "not being around anymore"
- Giving away possessions
- Saying goodbye in an unusual way
- Any direct or indirect mention of self-harm or suicide

SUBSTANCE USE INDICATORS:
- Smell of alcohol or marijuana
- Glassy or bloodshot eyes
- Slurred speech or coordination issues
- Mentioning drug or alcohol use by him or his peer group

SIGNS OF ABUSE OR UNSAFE HOME:
- Flinching at sudden movements
- Fear of going home
- Extreme emotional reactions around family topics
- Unexplained absences from school

WHAT TO SAY WHEN YOU NOTICE SOMETHING:
You do not have to confront or interrogate. A simple, caring question opens the door:
"You seem a little off today. Everything okay at home?"
"I've noticed you seem down lately — I'm here if you want to talk about it."

AFTER THE CONVERSATION:
If anything raises concern, report it to your program coordinator immediately — do not wait. Document what you observed (what you saw or heard, when, the context). You do not need to investigate further.

REMEMBER:
You are not alone in this. The program has trained staff to handle these situations. Your job is to notice, care, and connect — then hand it off to the right people.

REFLECTION QUESTIONS:
- Have you noticed any concerning behavior in your mentee that you have not yet addressed?
- What would it take for you to make the call to a program coordinator today if needed?
