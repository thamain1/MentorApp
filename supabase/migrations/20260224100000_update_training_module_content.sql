SETTING GOALS TOGETHER

Goals give the mentoring relationship direction. Without them, sessions can drift into casual conversation that feels good but produces little growth. Learning to set goals together — collaboratively — is a core mentoring skill.

WHY COLLABORATIVE GOAL-SETTING:
Goals handed down from mentor to mentee rarely stick. Goals discovered together, with the mentee in the driver's seat, create ownership and motivation. Your job is to help him articulate what he already wants and build a path to get there.

THE SMART FRAMEWORK:
Effective goals are:
S — Specific (not "get better at school," but "raise my math grade from a C to a B")
M — Measurable (can you track progress?)
A — Achievable (stretching but realistic)
R — Relevant (connected to what he actually cares about)
T — Time-bound (by when?)

THE GOAL DISCOVERY CONVERSATION:
Start with questions, not suggestions.
- "If everything went perfectly in the next six months, what would be different for you?"
- "What is one area of your life you wish was better?"
- "What do you want to be true about yourself in five years?"

Then help him narrow down and make it concrete:
- "That's a great goal. What would it look like if you were making progress on it?"
- "What is one small thing you could do this week that moves you in that direction?"

SHORT-TERM VS LONG-TERM GOALS:
Help your mentee have both. Long-term goals (college, career, character) give direction. Short-term goals (this week, this month) build momentum and confidence through small wins.

ACCOUNTABILITY WITHOUT NAGGING:
Check in on goals naturally at the start of sessions — "Last time you said you were going to talk to your teacher. How did that go?" Frame it as curiosity, not a report card. Celebrate progress. Explore obstacles without judgment.

WHEN GOALS CHANGE:
Goals evolve. That is fine. If a goal no longer fits, revisit it together. What changed? What does he want now? Flexibility keeps the relationship honest.

COMMON MISTAKES:
- Setting too many goals at once (stick to 1-3)
- Making goals too vague to measure
- Your goals for him vs his goals for himself
- Forgetting to revisit goals in future sessions

REFLECTION QUESTIONS:
- What is a goal you set for yourself that someone else helped you achieve?
- How do you handle it when a mentee does not follow through on a goal?
