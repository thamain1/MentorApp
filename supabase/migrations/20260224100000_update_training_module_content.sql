NAVIGATING CHALLENGES

Every mentoring relationship hits rough patches. Sessions get awkward. Your mentee goes quiet for weeks. A difficult topic comes up. Knowing how to handle these moments is what separates good mentors from great ones.

COMMON CHALLENGES AND HOW TO HANDLE THEM:

1. YOUR MENTEE PULLS BACK
Signs: shorter responses, canceled sessions, one-word answers.
What to do: Do not take it personally or pull away yourself. Reach out with low-pressure contact. "Hey, thinking about you. No pressure — just checking in." Give him space but remain consistent. Pulling back is often a test.

2. HE OPENS UP ABOUT SOMETHING HEAVY
A family crisis, abuse, substance use, mental health struggle, or thoughts of self-harm.
What to do: Stay calm. Do not react with panic or judgment. Say: "I'm glad you told me. That took courage." Then follow your reporting procedures. You do not have to have all the answers — your calm presence is the answer.

3. THE CONVERSATION DIES
Silence happens. It does not mean failure.
What to do: Have a few go-to questions ready. "What's something you're looking forward to?" or "What's been the hardest part of your week?" Do not force depth — sometimes showing up and watching a game together is the session.

4. HE PUSHES YOUR ADVICE BACK
"That won't work for me" or "You don't understand."
What to do: Good. Pushback means he is engaged. Ask more questions instead of defending your advice. "What would work better for you?" His ownership of the solution matters more than being right.

5. CULTURAL OR GENERATIONAL GAPS
You grew up differently. His world may be very different from yours.
What to do: Lean into curiosity, not judgment. "Help me understand that — I want to get it." You do not need to understand everything. You need to respect his experience.

6. MANAGING YOUR OWN FRUSTRATION
Some days you will feel like you are not getting through. Progress is invisible until it is not.
What to do: Take a long view. Seeds planted now may not sprout for years. Talk to your program coordinator if you need support — you should not carry this alone.

WHEN TO INVOLVE PROGRAM ADMIN:
- Any safety concern
- Consistent no-shows (more than two in a row without contact)
- Feeling overwhelmed or unsure how to handle something
- A significant change in your mentee's behavior or circumstances

YOU DO NOT HAVE TO HAVE ALL THE ANSWERS:
"I don't know, but let's figure it out together" is one of the most powerful things a mentor can say.

REFLECTION QUESTIONS:
- What is a challenging moment you have already experienced with your mentee?
- What is your default reaction when you feel frustrated — and what would a better response look like?
