DIGITAL SAFETY

Digital communication is where many of the greatest risks in mentoring relationships arise — and where clear guidelines matter most. This module establishes best practices for keeping all digital interaction safe, appropriate, and program-compliant.

WHY DIGITAL SAFETY MATTERS:
Digital communication leaves a permanent record. Inappropriate messaging — even with good intentions — can be misinterpreted, taken out of context, or violate program policy. It also models healthy digital behavior for your mentee.

APPROVED CHANNELS ONLY:
All communication between mentor and mentee must happen through the Iron Sharpens Iron app. This includes:
- Session scheduling
- Check-in messages
- Sharing resources

Do not communicate through personal text, WhatsApp, Snapchat, Instagram DMs, or any other platform. No exceptions.

SOCIAL MEDIA BOUNDARIES:
- Do not accept or send friend requests to your mentee on personal social media
- Do not follow your mentee on Instagram, TikTok, or similar platforms
- Do not view or comment on your mentee's personal social media content
- If he follows or tags you publicly, politely redirect: "Let's keep our connection through the app."

CONTENT STANDARDS:
- Keep all messages professional and appropriate
- Do not share personal photos, memes with adult content, or anything you would not show to the program director
- Do not discuss romantic relationships, personal finances, or personal problems in messages

RESPONSE TIME EXPECTATIONS:
You do not have to be available 24/7. Set healthy expectations early:
"I usually respond to messages within 24 hours on weekdays."
Respond to all messages, even if just to acknowledge you received it.

PHOTOS AND MEDIA:
- Do not photograph your mentee without his and his guardian's knowledge
- Do not post photos of your mentee on your personal social media
- Any photos taken at program events should be shared only through program channels

WHAT TO DO IF DIGITAL BOUNDARIES ARE CROSSED:
- If your mentee reaches out on personal channels, redirect him to the app and do not respond via the other channel
- Report any inappropriate messages or images received to program admin immediately — do not delete them
- If you accidentally make contact outside approved channels, disclose it to your coordinator

TALKING DIGITAL SAFETY WITH YOUR MENTEE:
Use this topic as a teachable moment. Discuss online safety, digital footprints, and how to protect themselves online — topics that matter deeply for their generation.

REFLECTION QUESTIONS:
- Are there any current communications with your mentee happening outside the approved app?
- How comfortable are you having a conversation with your mentee about their own digital safety?
