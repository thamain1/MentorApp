REPORTING PROCEDURES

Knowing when and how to report a concern is one of the most important responsibilities you have as a mentor. Reporting is not a betrayal of trust — it is an act of love.

WHEN TO REPORT:
Report to your program coordinator immediately if your mentee:
- Discloses abuse (physical, sexual, emotional, or neglect)
- Expresses thoughts of harming himself or others
- Is in or describes an unsafe living situation
- Shows signs of substance use or is in danger due to it
- Violates another person's safety
- Has gone missing or is in unknown whereabouts

You do not need to be 100% certain before reporting. If you are concerned, report it. It is better to report and be wrong than to stay silent and be right.

MANDATORY REPORTING:
In most jurisdictions, anyone working with minors who has reasonable suspicion of abuse or neglect is legally required to report it — even if you only suspect it. The program will guide you through the proper reporting channels.

HOW TO REPORT TO PROGRAM ADMIN:
1. Contact your program coordinator by phone or the app as soon as possible
2. Describe what you observed or what was shared with you — use specific words and details
3. Note the date, time, and context
4. Do not edit or interpret — report what you actually saw or heard
5. Follow up in writing if the coordinator requests it

WHAT HAPPENS AFTER YOU REPORT:
Program staff will take it from there. They will determine whether to contact parents, school, or child protective services. You may be asked to provide additional information. Continue showing up for your mentee unless instructed otherwise.

HOW TO TALK TO YOUR MENTEE ABOUT IT:
Be honest without over-explaining. "I care about you too much to keep this between us. I'm going to let someone who can really help know what you told me. That's not me getting you in trouble — that's me taking you seriously."

PROTECTING CONFIDENTIALITY:
Only share what is reported with program staff — not other volunteers, family members, or friends. Information shared in a report stays within the response chain.

DOCUMENTATION BEST PRACTICE:
After any session where something concerning comes up, write a brief note to yourself — what was said, what you observed, the date. This helps if you are asked for details later.

DO NOT:
- Investigate the situation yourself
- Confront a parent, caregiver, or alleged abuser
- Promise your mentee you will not tell anyone
- Delay reporting because you are unsure

REFLECTION QUESTIONS:
- Do you know your program coordinator's direct contact information?
- Is there anything in your current relationship with your mentee that you have been unsure whether to report?
