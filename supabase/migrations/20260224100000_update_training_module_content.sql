ACTIVE LISTENING SKILLS

Most people listen to respond. Great mentors listen to understand. Active listening is one of the most powerful tools you have — and one of the rarest gifts you can give a young man.

THE HEAR METHOD:
H — Hear the words without interrupting
E — Empathize with the emotion behind the words
A — Ask a follow-up question to go deeper
R — Reflect back what you heard to confirm understanding

WHY ACTIVE LISTENING MATTERS:
When a young man feels truly heard, something shifts. He becomes more open, more honest, and more willing to examine his own choices. Many mentees have never had an adult sit down and genuinely listen to them without an agenda.

PRACTICAL TECHNIQUES:

1. ELIMINATE DISTRACTIONS
Phone away. Eye contact. Lean slightly forward. Your body language communicates whether you are truly present.

2. DO NOT INTERRUPT
Let him finish his thought completely before responding. Silence is okay. Do not rush to fill every pause.

3. USE OPEN-ENDED QUESTIONS
Instead of "Did that bother you?" ask "How did that make you feel?"
Instead of "Was the game good?" ask "Tell me about the game."
Questions that start with What, How, Tell me about, and Walk me through invite depth.

4. REFLECT AND PARAPHRASE
"What I'm hearing is that you felt disrespected when your coach called you out in front of the team. Did I get that right?"
This tells him you were paying attention and gives him a chance to correct or confirm.

5. VALIDATE WITHOUT FIXING
"That sounds really frustrating." is often more powerful than a solution.
Resist the urge to immediately fix, advise, or silver-lining his problems. Sit with him in it first.

WHAT ACTIVE LISTENING IS NOT:
- Waiting for your turn to talk
- Planning your advice while he is still speaking
- One-upping his story with your own ("That reminds me of when I...")
- Dismissing his feelings ("You'll get over it" or "It's not that serious")

SIGNS YOU ARE DOING IT RIGHT:
- He starts sharing more without you having to pull it out of him
- He says things like "Yeah, exactly" or "You get it"
- Sessions start going longer than planned because he does not want to stop talking

PRACTICE:
In your next session, set a personal goal: ask three open-ended questions and reflect back once before giving any advice. Notice what changes.

REFLECTION QUESTIONS:
- Who in your life has made you feel truly heard? What did they do?
- What is your biggest listening habit to work on — interrupting, fixing, or getting distracted?
