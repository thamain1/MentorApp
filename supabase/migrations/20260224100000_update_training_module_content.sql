BUILDING TRUST

Trust is the foundation of every effective mentoring relationship. Without it, your mentee will not open up, take risks, or act on your guidance. Building trust takes time — it cannot be rushed.

THE TRUST EQUATION:
Trust = Consistency + Reliability + Authenticity
Every time you show up when you say you will, follow through on what you promise, and are honest about your own struggles, you make a deposit into the trust account. Every time you cancel, forget, or lecture instead of listen, you make a withdrawal.

CONSISTENCY IS EVERYTHING:
Show up on time. Every time. Young men — especially those who have experienced abandonment or instability — are watching to see if you will disappear like others have. Your consistent presence is the message before you ever say a word.

HOW TO BUILD TRUST EARLY:
1. Be fully present — put your phone away during sessions
2. Remember details — follow up on things he mentioned last time
3. Keep what he shares confidential (within safety limits)
4. Admit when you do not know something or made a mistake
5. Do what you say you will do, every single time

CONFIDENTIALITY:
Be clear with your mentee early: what he shares stays between you — with one important exception. If he shares something that puts him or someone else at serious risk of harm, you are required to report it to the program. Tell him this upfront so there are no surprises. This honesty itself builds trust.

AUTHENTICITY OVER AUTHORITY:
Mentees do not need you to have it all figured out. They need you to be real. Share your own failures and what you learned from them. Vulnerability from you creates safety for him.

COMMON TRUST BREAKERS TO AVOID:
- Canceling sessions without notice
- Sharing what he told you with others
- Being on your phone during your time together
- Comparing him to other mentees or to yourself at his age
- Reacting with shock or judgment when he opens up

PATIENCE:
Some young men have been let down many times. They may test you — pushing back, going quiet, or acting like they do not care. Stay consistent. Trust is built in the ordinary moments, not grand gestures.

REFLECTION QUESTIONS:
- Think of a relationship in your life where trust was broken. What did it take to rebuild it?
- What is one thing you can do in your next session to demonstrate reliability?
