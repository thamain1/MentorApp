INTRODUCTION TO MENTORING

Welcome to Iron Sharpens Iron. You are stepping into one of the most powerful roles a man can fill — a mentor.

WHAT IS A MENTOR?
A mentor is a trusted guide and role model. You are not a therapist, a parent, or a problem-solver. Your role is to walk alongside a young man, share your experience, and help him discover his own potential.

YOUR ROLE HAS THREE DIMENSIONS:
1. Guide — Help your mentee navigate decisions, challenges, and transitions
2. Role Model — Demonstrate through your own life what character, discipline, and integrity look like
3. Trusted Advisor — Be a consistent, safe presence he can come to with anything

WHAT MENTORING IS NOT:
- It is not fixing or rescuing your mentee
- It is not doing the work for him
- It is not a one-way lecture — it is a two-way relationship
- It is not therapy or counseling — know your limits

THE IMPACT OF MENTORSHIP:
Research consistently shows that young men with mentors are more likely to graduate, avoid risky behavior, hold steady employment, and build healthy relationships. Your investment of time is one of the highest-return activities you can give.

THE RELATIONSHIP ARC:
Most mentoring relationships go through stages — getting to know each other, building trust, doing the real work, and eventually transitioning as your mentee grows into independence. Be patient. The first few sessions are about connection, not content.

REFLECTION QUESTIONS:
- Who was a mentor figure in your own life, formal or informal?
- What do you hope your mentee will say about this experience five years from now?
- What strengths do you bring to this role?

NEXT STEPS:
Complete your first session with your mentee this week. Focus entirely on getting to know him — his interests, his goals, his story. Do not try to teach anything yet. Just listen.
